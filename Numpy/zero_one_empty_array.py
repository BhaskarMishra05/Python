import numpy as np
array_zero=np.zeros(9) #np.zeros(number of elements)
print("Zero array: ",array_zero)
print()
array_ones=np.ones(9) #np.ones(number of elements)
print("One array: ",array_ones)
print()
array_empty=np.empty(9) #Empty array shows the data of previous array.
print("Empty array: ",array_empty)